package com.duaz.microservices.admin.model;

public class Function {

    private Integer functionNo;
    private String functionName;
    private Integer isEngagement;
    
	public Integer getFunctionNo() {
		return functionNo;
	}
	public void setFunctionNo(Integer functionNo) {
		this.functionNo = functionNo;
	}
	public String getFunctionName() {
		return functionName;
	}
	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}
	public Integer getIsEngagement() {
		return isEngagement;
	}
	public void setIsEngagement(Integer isEngagement) {
		this.isEngagement = isEngagement;
	}


}
